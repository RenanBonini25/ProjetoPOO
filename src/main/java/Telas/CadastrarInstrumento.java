    private void comboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboTipoActionPerformed

        if (comboTipo.getSelectedItem().equals("Cordas")) {
            ServicoInstrumento.habilitarPanel(panelCordas);
            ServicoInstrumento.desabilitarPanel(panelSopro);
            ServicoInstrumento.desabilitarPanel(panelPercussao);
        } else if (comboTipo.getSelectedItem().equals("Percussão")) {
            ServicoInstrumento.habilitarPanel(panelPercussao);
            ServicoInstrumento.desabilitarPanel(panelSopro);
            ServicoInstrumento.desabilitarPanel(panelCordas);
        } else if (comboTipo.getSelectedItem().equals("Sopro")) {
            ServicoInstrumento.habilitarPanel(panelSopro);
            ServicoInstrumento.desabilitarPanel(panelCordas);
            ServicoInstrumento.desabilitarPanel(panelPercussao);
        }
    }//GEN-LAST:event_comboTipoActionPerformed

