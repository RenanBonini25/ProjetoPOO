    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed

        if (comboTipo.getSelectedItem().equals("Cordas")) {
            Cordas instrumento = new Cordas();
            instrumento.setNome(fieldNome.getText());
            instrumento.setMarca(fieldMarca.getText());
            instrumento.setCor(fieldCor.getText());
            try {
                instrumento.setPreco(Double.parseDouble(fieldPreco.getText().replaceAll(",", ".")));
                instrumento.setQuantidade(Integer.parseInt(fieldQuantidade.getText()));
            } catch (Exception ex) {
            }
            instrumento.setTipo((String) comboTipo.getSelectedItem());
            instrumento.setQtdCordas(fieldQtdCordas.getText());
            instrumento.setTipoBraco(fieldBraco.getText());
            instrumento.setTipoCorpo(fieldCorpo.getText());
            instrumento.setOrigem(fieldOrigem.getText());
            try {
                ServicoInstrumento.cadastrarInstrmento(instrumento);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(rootPane, "Instrumento cadastrado com sucesso!",
                    "Cadastro efetuado", JOptionPane.INFORMATION_MESSAGE);
        } else if (comboTipo.getSelectedItem().equals("Percussão")) {
            Percussao instrumento = new Percussao();
            instrumento.setNome(fieldNome.getText());
            instrumento.setMarca(fieldMarca.getText());
            instrumento.setCor(fieldCor.getText());
            try {
                instrumento.setPreco(Double.parseDouble(fieldPreco.getText().replaceAll(",", ".")));
                instrumento.setQuantidade(Integer.parseInt(fieldQuantidade.getText()));
            } catch (Exception ex) {
            }
            instrumento.setTipo((String) comboTipo.getSelectedItem());
            instrumento.setQtdPratos(fieldQtdPratos.getText());
            instrumento.setTipoMadeira(fieldMadeira.getText());
            instrumento.setDimensaoCaixa(fieldCaixa.getText());
            instrumento.setDimensaoToms(fieldToms.getText());
            try {
                ServicoInstrumento.cadastrarInstrmento(instrumento);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(rootPane, "Instrumento cadastrado com sucesso!",
                    "Cadastro efetuado", JOptionPane.INFORMATION_MESSAGE);
        } else if (comboTipo.getSelectedItem().equals("Sopro")) {
            Sopro instrumento = new Sopro();
            instrumento.setNome(fieldNome.getText());
            instrumento.setMarca(fieldMarca.getText());
            instrumento.setCor(fieldCor.getText());
            try {
                instrumento.setPreco(Double.parseDouble(fieldPreco.getText().replaceAll(",", ".")));
                instrumento.setQuantidade(Integer.parseInt(fieldQuantidade.getText()));
            } catch (Exception ex) {
            }
            instrumento.setTipo((String) comboTipo.getSelectedItem());
            instrumento.setMaterial(fieldMaterial.getText());
            instrumento.setAcabamento(fieldAcabamento.getText());
            instrumento.setAfinacao(fieldAfinacao.getText());
            try {
                ServicoInstrumento.cadastrarInstrmento(instrumento);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(rootPane, "Instrumento cadastrado com sucesso!",
                    "Cadastro efetuado", JOptionPane.INFORMATION_MESSAGE);
        }
        limparCampos();
    }//GEN-LAST:event_botaoSalvarActionPerformed

